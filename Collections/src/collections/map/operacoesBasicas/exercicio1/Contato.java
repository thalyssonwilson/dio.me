package collections.map.operacoesBasicas.exercicio1;

public class Contato {
    // atributos
    private String nome;
    private int telefone;


    // construtor
    public Contato(String nome, int telefone) {
        this.nome = nome;
        this.telefone = telefone;
    }


    // GET
    public String getNome() {
        return nome;
    }
    public int getTelefone() {
        return telefone;
    }

    
}
